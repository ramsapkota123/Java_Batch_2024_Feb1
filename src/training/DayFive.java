public class DayFive {
	public static void main(String[] args) {
		//conditional statement
		//one input multiple outputs
		
		int num=100;
		if(num>0 && num<=5) {
			System.out.println("10% discount");
		}
		if(num>5 && num<=10) {
			System.out.println("20% discount");
		}
		if(num>10) {
			System.out.println("30 % discount");
		}

		
	}

}
